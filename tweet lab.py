tweet = input('Enter abbreviation from tweet:\n')

if tweet == 'LOL':
    print('LOL = laughing out loud')
elif tweet == 'BFN':
    print('BFN = bye for now')
elif tweet == 'FTW':
    print('FTW = for the win')
elif tweet == 'IRL':
    print('IRL = in real life')
elif tweet == 'BTW':
    print('BTW = by the way')
elif tweet == 'CU':
    print('CU = see you')
elif tweet == 'AFAIK':
    print('AFAIK = as far as I know')
elif tweet == 'IDK':
    print('IDK = I do not know') 
else:
    print("Sorry, don't know that one")
